function buildPackageSearchText(pkg) {
  return [
    pkg.id,
    pkg.description,
    pkg.client,
    pkg.category,
    pkg.routine,
    pkg.status,
    pkg.criticality,
    pkg.monitorArriba,
    pkg.clientWeb,
    pkg.legacyPath,
    ...(pkg.keywords || [])
  ].filter(Boolean).join(" ");
}

window.ARRIBA_PACKAGE_INDEX = ARRIBA_PACKAGE_SOURCE_ROWS.map((pkg) => {
  const searchText = buildPackageSearchText(pkg);
  return {
    ...pkg,
    id_pacote: pkg.id,
    descricao: pkg.description,
    cliente: pkg.client,
    categoria: pkg.category,
    criticidade: pkg.criticality,
    monitorar_arriba: pkg.monitorArriba,
    qtde_passos: pkg.steps,
    qtde_passos_arquivo: pkg.fileSteps,
    pasta_destino_arquivo: pkg.legacyPath,
    searchText,
    normalizedSearchText: window.ARRIBA_NORMALIZE_PACKAGE_TEXT(searchText)
  };
});
